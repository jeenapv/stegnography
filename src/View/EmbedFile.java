    class progressThread extends Thread {

        public void run() {
            try {
                int value = 0;
                while (value <= 100) {
                    progress_bar.setValue(value);
                    value += 1;
                    Thread.sleep(10);
                }
                store_analyse_masterfile();
                proceed_button.setEnabled(true);
                analyze_complete_label.setVisible(true);
                sucess_label.setVisible(true);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

